'use strict';

console.log('Inequality operator');
console.log(10 != 20);
console.log('20' != 20);
console.log(null != undefined);

console.log('Strict Inequality or Nonidentity operator');
console.log(10 !== 20);
console.log('20' !== 20);
console.log(null !== undefined);
console.log(1 !== true);
console.log(10 !== 10);